package main.java.component;

public class Car {
	private String name, model, color;
	private int date, price;

	public Car(String name, String model, String color, int date, int price) {
		super();
		this.name = name;
		this.model = model;
		this.color = color;
		this.date = date;
		this.price = price;
	}

}
